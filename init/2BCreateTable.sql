CREATE TABLE `dealership`.`cars`
  (
     `idcars`     INT,
     `make`       VARCHAR(64),
     `model`      VARCHAR(64),
     `carpackage` VARCHAR(64),
     `color`      VARCHAR(64),
     `year`       INT,
     `category`   VARCHAR(64),
     `mileage`    INT,
     `price`      INT,
     PRIMARY KEY (`idcars`)
  ); 